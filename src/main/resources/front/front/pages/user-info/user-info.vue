                        // 下拉变化
                        bisaixuanshouxingbieChange(e) {
                                this.bisaixuanshouxingbieIndex = e.target.value
                                this.ruleForm.xingbie = this.bisaixuanshouxingbieOptions[this.bisaixuanshouxingbieIndex]
