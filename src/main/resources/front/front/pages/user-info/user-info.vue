				bisaixuanshouxingbieIndex: 0,
			}
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			}
		},
		async onLoad() {
			let table = uni.getStorageSync("nowTable");
			let res = await this.$api.session(table);
			this.ruleForm = res.data;
			this.tableName = table;
			// 自定义下拉框值
			if(this.tableName=='guanzhong'){
				this.guanzhongxingbieOptions = "男,女".split(',');
				this.guanzhongxingbieOptions.forEach((item, index) => {
					if(item==this.ruleForm.xingbie) {
						this.guanzhongxingbieIndex = index;
					}
				});
			}
			// 自定义下拉框值
			if(this.tableName=='caipan'){
				this.caipanxingbieOptions = "男,女".split(',');
				this.caipanxingbieOptions.forEach((item, index) => {
					if(item==this.ruleForm.xingbie) {
						this.caipanxingbieIndex = index;
					}
				});
